const selectedChannelId = computed(() => route.params.channelId);

const filteredChannels = computed(() => {
  if (!searchQuery.value) return channelStore.channels;
  const query = searchQuery.value.toLowerCase();
  return channelStore.channels.filter(channel => 
    channel.name.toLowerCase().includes(query) || 
    channel.handle.toLowerCase().includes(query)
  );
});

function selectChannel(channelId: string) {
  router.push(`/channels/${channelId}`);
}

function openChannelSettings(channelId: string, event: Event) {
  event.stopPropagation();
  router.push(`/settings/channels/${channelId}/settings`);
}

function openNewChannelModal() {
  showNewChannelModal.value = true;
}

function closeNewChannelModal() {
  showNewChannelModal.value = false;
